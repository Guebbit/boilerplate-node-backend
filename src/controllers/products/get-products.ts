import type { Request, Response } from 'express';
import { z } from 'zod';
import {
    SearchProductsBody,
    searchProductsBodyPageDefault,
    searchProductsBodyPageSizeDefault,
    searchProductsBodyPageSizeMax,
    searchProductsBodyMinPriceMin,
    searchProductsBodyMaxPriceMin
} from '@api/schemas.zod';
import { productService } from '@services/products';
import { rejectResponse, successResponse } from '@utils/response';
import { extractId, extractStringList, mergeBodyQuery } from '@utils/helpers-request';
import type { SearchProductsRequest } from '@types';
import type { CastError } from 'mongoose';
import { emitAnalyticsEvent, AnalyticsEvent, buildAnalyticsBase } from '@utils/analytics';

/**
 * Query parameters generated by the search form (they are the same as SearchOrdersRequest keys)
 */
export type IGetProductsQuery = Partial<Record<keyof SearchProductsRequest, string>>;

/**
 * Built on the orval-generated SearchProductsBody (kept in sync with
 * openapi.yaml); page/pageSize/minPrice/maxPrice are coerced from strings
 * since GET requests carry them as query-string text, not JSON numbers.
 */
const searchProductsQuerySchema = SearchProductsBody.extend({
    page: z.preprocess(
        (value) =>
            value === '' || value === null || value === undefined
                ? searchProductsBodyPageDefault
                : value,
        z.coerce.number().min(1)
    ),
    pageSize: z.preprocess(
        (value) =>
            value === '' || value === null || value === undefined
                ? searchProductsBodyPageSizeDefault
                : value,
        z.coerce.number().min(1).max(searchProductsBodyPageSizeMax)
    ),
    minPrice: z.preprocess(
        (value) => (value === '' || value === null ? undefined : value),
        z.coerce.number().min(searchProductsBodyMinPriceMin).optional()
    ),
    maxPrice: z.preprocess(
        (value) => (value === '' || value === null ? undefined : value),
        z.coerce.number().min(searchProductsBodyMaxPriceMin).optional()
    )
});

/**
 * GET /products
 * POST /products/search
 * List/search products via query parameters or request body.
 * Admin sees all products (including inactive/deleted); public sees only active ones.
 */
export const getProducts = (
    request: Request<{ id?: string }, unknown, SearchProductsRequest, IGetProductsQuery>,
    response: Response
) => {
    const merged = mergeBodyQuery(
        request.body as Record<string, unknown> | undefined,
        request.query as Record<string, string> | undefined
    );
    // OpenAPI currently models category/tag as single-value filters; if arrays/CSV are provided we pick the first one.
    const category = extractStringList(merged.category)[0];
    const tag = extractStringList(merged.tag)[0];
    const id = extractId(request.params.id, merged.id as string | undefined);

    const parseResult = searchProductsQuerySchema.safeParse({ ...merged, id, category, tag });
    if (!parseResult.success)
        return rejectResponse(
            response,
            422,
            'getProducts - invalid data',
            parseResult.error.issues.map(({ message }) => message)
        );

    const { page, pageSize, text } = parseResult.data;

    return productService
        .search(parseResult.data, request.authContext?.admin === true)
        .then((result) => {
            emitAnalyticsEvent({
                ...buildAnalyticsBase(request),
                event: AnalyticsEvent.PRODUCTS_SEARCHED,
                properties: {
                    text,
                    page,
                    pageSize,
                    result_count: result.items.length
                }
            });
            successResponse(response, result);
        })
        .catch((error: CastError) => {
            rejectResponse(response, 500, 'getProducts', [error.message]);
        });
};
